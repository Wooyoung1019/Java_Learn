public class demo10 {
    public static void main(String[] args) {
        // 与(and)或(or) 非(取反)
        boolean a = true;
        boolean b = false;

        System.out.println("a && b : " + (a && b));
        System.out.println("a || b : " + (a || b));
        System.out.println("!a : " + (!a));

        System.out.println("a & b : " + (a & b));
        System.out.println("a | b : " + (a | b));
        System.out.println("a ^ b : " + (a ^ b));

        System.out.println(2<<1);
        System.out.println(3<<1);

        System.out.println(""+10+20);
        System.out.println(10+20+"");

        int score = 100;
     }
}
